        <div class="section">
            <h2>Introduction</h2>
            <div class="important-box">
                <p><strong>Safeguarding</strong> is the responsibility that an organization has to ensure that their employees and volunteers, partners, vendors, operations and programmes do no harm to children, young people or vulnerable adults.</p>
            </div>
            <p>Through their work, SMO employees, employees of partner organizations and volunteers may engage with young people and vulnerable adults either directly or indirectly.</p>
        </div>

        <div class="section">
            <h2>Purpose</h2>
            <p>The purpose of this policy and associated procedures is to provide clarity to <strong>ALL</strong> on how they should engage with children, young people and vulnerable adults when working for, on behalf of, or in partnership with SMO.</p>
        </div>

        <div class="section">
            <h2>Key Definitions</h2>
            <h3>Abuse</h3>
            <p>A violation of an individual's human and civil rights by any other person or persons. It can take various forms:</p>
            <ul>
                <li><strong>Discriminatory abuse</strong> - abuse motivated by a vulnerable person's age, race, nationality, sex, sexual orientation, disability, or other personal characteristic.</li>
                <li><strong>Financial or material abuse</strong> - including theft, fraud, exploitation, and pressure in connection with financial transactions.</li>
                <li><strong>Physical abuse</strong> - includes hitting, shaking, throwing, poisoning, burning or scalding.</li>
                <li><strong>Psychological abuse</strong> - includes emotional abuse, threats of harm or abandonment, deprivation of contact.</li>
                <li><strong>Sexual abuse</strong> - involves forcing or enticing participation in sexual activities.</li>
            </ul>
        </div>

        <div class="section">
            <h2>Policy Statement</h2>
            <div class="important-box">
                <p>SMO has zero tolerance against abuse and exploitation of vulnerable people.</p>
            </div>
            <h3>Key Principles</h3>
            <ul>
                <li>Everyone has an equal right to protection from abuse and exploitation</li>
                <li>The best interests of the vulnerable person are paramount</li>
                <li>SMO will take responsibility to meet our obligations regarding duty of care</li>
                <li>All employees and volunteers will be inducted in Safeguarding Standards</li>
                <li>Partners must comply with our Safeguarding Standards</li>
            </ul>
        </div>

        <div class="section">
            <h2>Responsibilities</h2>
            <h3>All people working with SMO will:</h3>
            <ul>
                <li>Read, understand and adhere to the SMO Safeguarding Policy and Code of Conduct Policy</li>
                <li>Promote a zero tolerance approach to discrimination, sexual harassment and abuse</li>
                <li>Develop relationships based on equality, trust, respect and honesty</li>
                <li>Place the safety and welfare of children and vulnerable people above all other considerations</li>
                <li>Report any concerns about the welfare of a vulnerable person</li>
            </ul>

            <h3>All people working with SMO will not:</h3>
            <ul>
                <li>Sexually harass, assault or abuse another person</li>
                <li>Physically harass, assault or abuse another person</li>
                <li>Emotionally abuse another person</li>
                <li>Condone or participate in abusive behavior</li>
                <li>Develop inappropriate relationships with vulnerable people</li>
            </ul>
        </div>

        <div class="section">
            <h2>Reporting Concerns</h2>
            <div class="important-box">
                <p>SMO places a <span class="highlight">mandatory obligation</span> on all employees, volunteers, contractors and partners to report concerns, suspicions, allegations and incidents which indicate actual or potential abuse or exploitation of vulnerable people.</p>
            </div>
            <p>It is not the responsibility of employees to decide whether abuse has taken place, but concerns should be raised with:</p>
            <ul>
                <li>Line manager</li>
                <li>Functional lead</li>
                <li>Designated safeguarding officer</li>
            </ul>
